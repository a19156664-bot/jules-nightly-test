#!/usr/bin/env bash
# =============================================================================
# .nightly/ci.sh — プロジェクト固有のCI実体(言語に合わせて書き換える)
# -----------------------------------------------------------------------------
# ci.yml から呼ばれる。exit 0 = 合格 / 非0 = 不合格。
# テスト未導入の場合、このファイルを削除しておけばブートストラップモードで
# 動作し、初夜にJulesへ「テスト基盤構築 + 本ファイルの実体化」を依頼できる。
#
# 例(必要な行のコメントを外して使う):
# --- Python ---
# pip install -r requirements.txt
# python -m pytest -q
# --- Node.js ---
# npm ci
# npm test
# --- Go ---
# go vet ./... && go test ./...
# =============================================================================
set -euo pipefail

echo "⚠️  .nightly/ci.sh はまだ雛形のままです。プロジェクトのテストコマンドに書き換えてください。"
echo "    (雛形のままでも exit 0 なので、パイプラインの疎通確認には使えます)"
exit 0
